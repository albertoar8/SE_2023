class operation {
  constructor() {}
  addition(num1, num2) {
    const value = num1 + num2;
    return value;
  }
  substraction(num1, num2) {
    const value = num1- num2;
    return value;
  }
  multiply(num1, num2) {
    const value = num1 * num2;
    return value;
  }
  divide(num1, num2) {
    const value = num1 / num2;
    return value;
  }
}
module.exports=operation;
